#include <bits/stdc++.h>
using namespace std;

// Write a program to convert temperature given in Celsius(user input) to Fahrenheit.
int main()
{
  int cel1 = 47;
  int cel2 = 29;
  cout << "47 deg Celsius in Fahrenheit: " << (9.0 / 5 * cel1 + 32) << endl;
  cout << "100 deg Celsius in Fahrenheit: " << (9.0 / 5 * cel2 + 32) << endl;
  return 0;
}